package FrontEnd;

public class ValuesAndValueType {
    private int val;
    private int type;

    public ValuesAndValueType(int val_in, int type_in) {
        this.val = val_in;
        this.type = type_in;
    }

    public int getVal() {
        return val;
    }

    public int getType() {
        return type;
    }
}
